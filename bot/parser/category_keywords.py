def detect_category(text: str, skip_free: bool = False) -> tuple[str, str | None]:
    """
    Визначає категорію та підкатегорію за текстом оголошення.
    skip_free=True — не відносити до free_stuff якщо є явна ціна.
    Повертає (category, subcategory).
    """
    lower = text.lower()
    best_category = "other"
    best_subcategory = None
    best_score = 0

    for category, subcategories in CATEGORY_KEYWORDS.items():
        if category == "free_stuff" and skip_free:
            continue

        for subcategory, keywords in subcategories.items():
            score = 0
            for kw in keywords:
                if re.search(r"\b" + re.escape(kw) + r"\b", lower) or kw in lower:
                    score += 1
            if score > best_score:
                best_score = score
                best_category = category
                best_subcategory = subcategory if subcategory and not subcategory.startswith("other_") else None

    return best_category, best_subcategory


